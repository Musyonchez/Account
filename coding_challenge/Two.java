        
public class Two {
    public static void main(String[] args) {
        // Increment and Decrement Order

        int number = 1;
        number++;
        System.out.println("First: " + number);
        System.out.println("Second: " + number++);
        System.out.println("Third: " + number);
        System.out.println("First: " + (++number));

        int counter = 1;
        ++counter;
        counter++;
        System.out.println("First: " + counter);

    }
}
