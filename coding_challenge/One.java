
public class One {
    public static void main(String[] args) {

        // Increment and Decrement
        int count = 1;
        count = count + 1;
        System.out.println("First Increment: " + count);
        count += 1;
        System.out.println("Second Increment: " + count);
        count++;
        System.out.println("Third Increment: " + count);
        count = count - 1;
        System.out.println("First Decrement: " + count);
        count -= 1;
        System.out.println("Second Decrement: " + count);
        count--;
        System.out.println("Third Decrement: " + count);

    }
}
